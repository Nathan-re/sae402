package fr.rey.dev.sae402;

public class Rondelle {

    private float x;
    private float y;

    public Rondelle(){
        x = 100;
        y = 100;
    }

    public float getX() {
        return x;
    }

    public void setX(float x) {
        this.x = x;
    }

    public float getY() {
        return y;
    }

    public void setY(float y) {
        this.y = y;
    }
}
